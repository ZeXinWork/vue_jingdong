import { createRouter, createWebHashHistory } from 'vue-router'
import Home from '../views/home'
import Login from '../views/login'

const routes = [
  {
    path: '/',
    name: 'Home',
    component: Home
  },
  {
    path: '/login',
    name: 'Login',
    component: Login
  }
]

const router = createRouter({
  history: createWebHashHistory(),
  routes
})

export default router
